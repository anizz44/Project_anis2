    <h3>LEE JIHOON PUNCAK LOOKISM</h3>

    <p> 
        Lee Jihoon merupakan seorang tokoh yang dianggap legenda oleh para karakter-karakter di seri ini. 
        Dia mendapat julukan tersebut karena ia berhasil mengalahkan orang-orang kuat yang disebut sebagai raja dari tiap-tiap daerah yang ada di Korea. 
        Dalam hal ini Lee Jihoon merupakan orang yang berasal dari Generasi 1, berbeda dengan MC yang merupakan tokoh dari Generasi 2. 
        Hal tersebut yang menjadi alasan mengapa MC tidak bertemu dan berhadapan langsung dengan Lee Jihoon di chapter awal.
    </p>
    <p>
        Lee Jihoon merupakan orang yang bisa dikatakan sempurna. Selain pandai dalam berkelahi, ia juga pandai dalam berbagai hal. 
        Terbukti dengan ditampilkannya piala-piala kejuaraan yang berhasil dimenangkan Lee Jihoon dengan predikat juara satu, dan piala tersebut tidak bisa dihitung jumlahnya. 
        Benar-benar orang yang sempurna
    </p>
    <p>
        Kekuatan Lee Jihoon awalnya tidak diperlihatkan dalam chapter-chapter lookism.
        Para pembaca hanya dibuat agar berasumsi seolah-olah Lee Jihoon merupakan orang terkuat, jika dilihat dari flashback yang ditampilkan. 
        Dan pada Flashback tersebut tidak ditampilkan bagaiman Lee Jihoon berkelahi, hanya ditampilkan jika semua perkelahian selalu dimenangkan oleh Lee Jihoon tanpa mendapat luka sedikitpun. 
        Karena rekor kemenangannya itu, Lee Jihoon mampu menghentikan era Generasi satu dan membuat era baru yakni Generasi dua, generasi dimana MC berada.
    </p>
    <p>
        Setelah berjalan 400 chapter lebih, baru diketahui jika Lee Jihoon merupakan orang yang membunuh tokoh terkuat pada generasi 0, yakni Kim Gabryong. 
        Kim Gabryong merupakan orang yang memimpin sebuah gang yang sangat kuat pada era nya. Nama-nama seperti Lee Dogyu, Park Jinyoung, 
        serta Elite merupakan nama-nama yang sampai saat chapter terbarunya muncul, nama tersebut masih sering disebut-sebut.
    </p>
